describe("测试一个模块", () => {
  function add(a, b) {
    return a + b;
  }

  test('3+2=5', () => {
    expect(add(3, 2)).toBe(5)
  });

  test('3+2<6', () => {
    expect(add(3, 2)).toBeLessThan(6)
  });
});
